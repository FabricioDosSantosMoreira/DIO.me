# │
# ├───────> Gerenciamento de Pacotes com 'poetry':
# │
# │             Poetry é uma ferramenta de gerenciamento de dependências e  
# │         empacotamento para projetos em Python. Ele visa simplificar e
# │         facilitar o processo de desenvolvimento, distribuição 
# │         e publicação de pacotes Python.
# │
# │
# └───┬───> Exemplos de Utilização do 'poetry':
#     │
#     ├───> Exemplo de Instalação:
#     │         
#     │         Para instalar o poetry, você pode usar o pip, que é o gerenciador de 
#     │     pacotes padrão do Python. Abra o terminal e execute o seguinte comando:
#     │         
#     │         * pip install poetry
#     │         NOTE: Este comando instala o pacote 'poetry'
#     │
#     ├───> Exemplo de Criação de um Projeto:
#     │         
#     │         Para criar um novo projeto, navegue até o diretório do seu
#     │     projeto e execute o segunte comando:
#     │         
#     │         * poetry new meu_projeto
#     │         NOTE: Este comando criará uma estrutura básica para o projeto, incluindo arquivos de 
#     │         configuração pyproject.toml e um diretório para o código-fonte
#     │
#     ├───> Exemplo de Adição de Dependências:
#     │         
#     │         Para adicionar dependências ao projeto, execute o segunte comando:
#     │         
#     │         * poetry add requests
#     │         NOTE: Este comando adicionará a dependência 'requests' ao arquivo pyproject.toml e
#     │         instalará a  dependência no ambiente virtual do projeto
#     │
#     ├───> Exemplo de Adição de Dependências de Desenvolvimento:
#     │         
#     │         Para adicionar dependências de desenvolvimento ao projeto, execute o segunte comando:
#     │         
#     │         * poetry add --dev requests
#     │         NOTE: Este comando adicionará a dependência como uma dependência de desenvolvimento
#     │        
#     ├───> Exemplo de Instalação de Dependências:
#     │         
#     │         Para instalar todas as dependências do projeto, execute o segunte comando:
#     │         
#     │         * poetry install
#     │         NOTE: Este comando instalará todas as dependências listadas no arquivo pyproject.toml
#     │             
#     ├───> Exemplo de Execução de Scripts:
#     │         
#     │         Para executar um script ou comando dentro do ambiente virtual do poetry, 
#     │     execute o segunte comando:
#     │
#     │         * poetry run meu_comando
#     │         NOTE: Este comando garante que o comando seja executado dentro do contexto do  
#     │         ambiente virtual do projeto
#─────┘
