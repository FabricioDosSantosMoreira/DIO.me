# ├───────> Ánalise de Código com 'flake8':
# │
# │             O flake8 é uma ferramenta que analisa o código Python em busca de 
# │         possíveis erros, violações de estilo e outros problemas. Ele segue as  
# │         diretrizes definidas no PEP 8, o guia de estilo oficial do Python. O
# │         uso do flake8 ajuda a manter o código Python consistente e legível, 
# │         facilitando a colaboração e reduzindo possíveis erros.
# │ 
# │ 
# └───┬───> Exemplos de Utilização do 'flake8':
#     │
#     ├───> Exemplo de Instalação:
#     │
#     │         Para instalar o flake8, você pode usar o pip, que é o gerenciador de
#     │     pacotes padrão do Python. Abra o terminal e execute o seguinte comando:
#     │
#     │         * pip install flake8
#     │         NOTE: Este comando instala o pacote 'flake8'
#     │
#     ├───> Exemplo de Utilização:
#     │
#     │         Para analisar o código de um arquivo Python, basta executar o `flake8` 
#     │     seguido do caminho para o arquivo. Por exemplo:
#     │
#     │         * flake8 meu_codigo.py
#     │         NOTE: Este comando executa o 'flake8' no arquivo 'meu_codigo.py'
#     │
#     ├───> Exemplo de Saída:
#     │
#     │         * meu_codigo.py:5:1: E302 expected 2 blank lines, found 1
#     │         NOTE: O arquivo 'meu_codigo.py' contém duas linha em branco
#─────┘
