# │
# ├───────> Formatação de Código com 'black':
# │
# │             Black é uma ferramenta de formatação de código Python que aplica um estilo
# │         consistente ao seu código automaticamente. Ele formata o código de acordo com 
# │         um conjunto de regras predefinidas, garantindo que o código seja legível e 
# │         consistente em toda a base de código.
# │
# │
# └───┬───> Exemplos de Utilização do 'black':
#     │
#     ├───> Exemplo de Instalação:
#     │
#     │         Para instalar o black, você pode usar o pip, que é o gerenciador de
#     │     pacotes padrão do Python. Abra o terminal e execute o seguinte comando:
#     │
#     │         * pip install black
#     │         NOTE: Este comando instala o pacote 'black'
#     │
#     ├───> Exemplo de Utilização:
#     │
#     │         Para formatar o código de um arquivo Python, basta executar o `black` 
#     │     seguido do caminho para o arquivo. Por exemplo:
#     │  
#     │         * black meu_codigo.py
#     │         NOTE: Este comando executa o 'black' no arquivo 'meu_codigo.py' e irá
#     │         reformatar o arquivo Python de acordo com as regras do black
#     │
#     ├───> Exemplo de Opções de Utilização:
#     │
#     │         O Black oferece várias opções para personalizar o processo de formatação
#     │     do código. Como por exemplo:
#     │  
#     │         * '--check': Verifica se o código está formatado corretamente sem fazer alterações
#     │         * '--exclude': Especifica pastas ou arquivos a serem excluídos da formatação
#     │         * '--line-length': Define o limite de largura de linha desejado   
#─────┘
