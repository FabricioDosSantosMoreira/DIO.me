# │
# ├───────> Ordenação de Importações com 'isort':
# │
# │             A ordenação de imports é uma prática comum em projetos Python 
# │         para manter a consistência e legibilidade do código. O `isort` é 
# │         uma ferramenta que automatiza esse processo, organizando os 
# │         imports de acordo com convenções estabelecidas.
# │
# │
# └───┬───> Exemplos de Utilização do 'isort':
#     │
#     ├───> Exemplo de Instalação:
#     │
#     │         Para instalar o isort, você pode usar o pip, que é o gerenciador de
#     │     pacotes padrão do Python. Abra o terminal e execute o seguinte comando:
#     │
#     │         * pip install isort
#     │         NOTE: Este comando instala o pacote 'isort'
#     │
#     ├───> Exemplo de Utilização:
#     │
#     │         Para ordenar os imports de um arquivo Python, basta executar o `isort` 
#     │         seguido do caminho para o arquivo. Exemplo:
#     │  
#     │         * isort meu_codigo.py
#     │         NOTE: Este comando executa o 'isort' no arquivo 'meu_codigo.py' e irá
#     │         reorganizar os imports
#─────┘
