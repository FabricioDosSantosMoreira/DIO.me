# │
# ├───────> Guia de Estilo da 'PEP8':
# │
# │             A PEP8 é uma guia de estilo para código Python, que visa melhorar a 
# │         legibilidade e consistência do código fonte. Ela define recomendações  
# │         sobre formatação, convenções de nomes, comentários, entre outros aspectos.
# │
# │
# └───┬───> Recomendações da 'PEP8':
#     │
#     ├───> Recomendações Gerais:
#     │         
#     │         Algumas recomendações gerais da PEP8 incluem:
#     │ 
#     │             * Utilizar indentação de 4 espaços
#     │             * Utilizar espaços em vez de tabs
#     │             * Limitar linhas a 79 caracteres
#     │             * Separar funções e classes por duas linhas em branco
#     │
#     ├───> Recomendações de Nomeação de Variáveis e Funções:
#     │         
#     │         Algumas recomendações sobre convenções de nomeação:
#     │ 
#     │             * Utilizar snake_case para nomes de variáveis e funções
#     │             * Utilizar nomes de constantes com letras maiúsculas
#     │      
#     ├───> Recomendações de Comentários:
#     │         
#     │         Algumas recomendações sobre como comentar o código de forma clara e concisa:
#     │ 
#     │             * Utilizar comentários para explicar o código quando necessário
#     │             * Utilizar docstrings para documentar funções e módulos
#     │             * Limitar linhas de comentário a 72 caracteres.
#─────┘
