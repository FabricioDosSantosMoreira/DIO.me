# - - - - - > Tipos de Dados:
#                 Tipos de dados são categorias que especificam quais valores 
#             podem ser armazenados e manipulados. Esses tipos de dados são 
#             fundamentais para manipular informações e realizar operações. 
#             Cada tipo de dado possui características específicas e 
#             métodos associados para manipulação e processamento.


# - - - - > Exemplos de Tipos de Dados:
print("\nExemplos de Tipos de Dados:")

# - - - > Inteiro (integer):
valor_int: int = 10

print("\nInteiro (integer):", valor_int)
print(type(valor_int))


# - - - > Ponto flutuante (float):
valor_float: float = 3.14

print("\nPonto flutuante (float):", valor_float)
print(type(valor_float))


# - - - > Cadeia de caracteres (string):
valor_str: str = "Olá, mundo!"

print("\nCadeia de caracteres (string):", valor_str)
print(type(valor_str))


# - - - > Booleano (bool):
valor_booleano: bool = True

print("\nBooleano (bool):", valor_booleano)
print(type(valor_booleano))


# - - - > Lista (list):
valor_lista: list = [1, 2, 3, 4, 5]

print("\nLista (list):", valor_lista)
print(type(valor_lista))


# - - - > Tupla (tuple):
valor_tupla = (1, 2, 3, 4, 5)

print("\nTupla (tuple):", valor_tupla)
print(type(valor_tupla))


# - - - > Dicionário (dict):
valor_dicionario: dict = {'a': 1, 'b': 2, 'c': 3}

print("\nDicionário (dict):", valor_dicionario)
print(type(valor_dicionario))


# - - - > Conjunto (set):
valor_conjunto: set = {1, 2, 3, 4, 5}

print("\nConjunto (set):", valor_conjunto)
print(type(valor_conjunto))


# - - - > Nulo (None):
valor_nulo: None = None

print("\nNulo (None):", valor_nulo)
print(type(valor_nulo))


# - - - > Bytes (bytes):
valor_bytes: bytes = b'Hello'

print("\nBytes (bytes):", valor_bytes)
print(type(valor_bytes))


# - - - > Bytearray (bytearray):
valor_bytearray: bytearray = bytearray(b'Hello')

print("\nBytearray (bytearray):", valor_bytearray)
print(type(valor_bytearray))
