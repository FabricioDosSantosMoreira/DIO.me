# - - - - - > Operadores de Atribuição:
#                 Operadores de atribuição são utilizados para atribuir valores a variáveis.
#             Eles permitem associar um valor a uma variável de forma simples e direta. 


# - - - - > Exemplos de Operadores de Atribuição:
print("\nExemplos de Operadores de Atribuição:")

# - - - > Operador de atribuição simples (=):
x: int = 10 

print("\nOperador de atribuição simples (=):")
print(f"Resultado [x = 10]: {x}") 


# - - - - > Exemplos de Operadores de Atribuição Combinada (+=, -=, *=, /=, %=, **/, //=):
# NOTE: Estes operadores combinam atribuição com uma operação aritmética
print("\nOperadores de Atribuição Combinada (+=, -=, *=, /=, %=, **/, //=):")

# - - - > Operador de atribuição e adição (+=):
x: int = 10 
x += 5  # NOTE: Equivalente a x = x + 5

print("\nOperador de atribuição e adição (+=):")
print(f"Resultado [x += 5]: {x}") 


# - - - > Operador de atribuição e subtração (-=):
x: int = 10 
x -= 3  # NOTE: Equivalente a x = x - 3

print("\nOperador de atribuição e subtração (-=):")
print(f"Resultado [x -= 5]: {x}") 


# - - - > Operador de atribuição e multiplicação (*=):
x: int = 10 
x *= 2  # NOTE: Equivalente a x = x * 2

print("\nOperador de atribuição e multiplicação (*=):")
print(f"Resultado [x *= 2]: {x}") 


# - - - > Operador de atribuição e divisão (/=):
x: int = 10 
x /= 4  # NOTE: Equivalente a x = x / 4

print("\nOperador de atribuição e divisão (/=):")
print(f"Resultado [x /= 4]: {x}") 


# - - - > Operador de atribuição e resto da divisão (%):
x: int = 10 
x %= 2  # NOTE: Equivalente a x = x % 2

print("\nOperador de atribuição e resto da divisão (%):")
print(f"Resultado [x %= 2]: {x}") 


# - - - > Operador de atribuição e potenciação (**):
x: int = 10 
x **= 3  # NOTE: Equivalente a x = x ** 3

print("\nOperador de atribuição e potenciação (**):")
print(f"Resultado [x **= 3]: {x}") 


# - - - > Operador de atribuição e divisão inteira (//):
x: int = 10 
x //= 2  # NOTE: Equivalente a x = x // 2

print("\nOperador de atribuição e divisão inteira (//):")
print(f"Resultado [x //= 2]: {x}") 


# - - - > Operador de atribuição e concatenação (+=):
texto: str = "Olá"
texto += " mundo!"  # NOTE: Equivalente a texto = texto + " mundo!"

print("\nOperador de atribuição e concatenação (+=):")
print(f"Resultado [texto += ' mundo!']: {texto}") 
