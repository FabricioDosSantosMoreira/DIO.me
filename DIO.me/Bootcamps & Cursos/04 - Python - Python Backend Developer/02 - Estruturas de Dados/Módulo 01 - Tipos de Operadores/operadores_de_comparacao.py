# - - - - - > Operadores de Comparação:
#                 Operadores de comparação são usados para comparar valores em Python. 
#             Eles retornam True se a comparação for verdadeira e False se for falsa.


# - - - - > Exemplos de Operadores de Comparação:
print("\nExemplos de Operadores de Comparação:")

a: int = 5
b: int = 5
c: int = 7

# - - - > Operador de comparação de igualdade (==):
print("\nOperador de comparação de igualdade (==):")
print(f"Resultado [a == b]: {a == b}")  
print(f"Resultado [a == c]: {a == c}")  


# - - - > Operador de comparação de desigualdade (!=):
print("\nOperador de comparação de desigualdade (!=):")
print(f"Resultado [a != b]: {a != b}")  
print(f"Resultado [a != c]: {a != c}")  


# - - - > Operador de comparação maior que (>):
print("\nOperador de comparação maior que (>):")
print(f"Resultado [c > a]: {c > a}")  
print(f"Resultado [a > c]: {a > c}")  


# - - - > Operador de comparação menor que (<):
print("\nOperador de comparação menor que (<):")
print(f"Resultado [a < c]: {a < c}")  
print(f"Resultado [c < a]: {c < a}")  


# - - - > Operador de comparação maior ou igual que (>=):
print("\nOperador de comparação maior ou igual que (>=):")
print(f"Resultado [c >= a]: {c >= a}") 
print(f"Resultado [a >= c]: {a >= c}")  


# - - - > Operador de comparação menor ou igual que (<=):
print("\nOperador de comparação menor ou igual que (<=):")
print(f"Resultado [a <= c]: {a <= c}")  
print(f"Resultado [c <= a]: {c <= a}") 
