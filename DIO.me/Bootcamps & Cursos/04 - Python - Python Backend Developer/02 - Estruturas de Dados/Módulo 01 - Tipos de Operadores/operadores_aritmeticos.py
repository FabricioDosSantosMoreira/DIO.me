# - - - - - > Operadores Aritméticos:
#                 Operadores aritméticos são símbolos utilizados para realizar operações matemáticas
#             básicas em variáveis ou valores. Eles incluem: adição, subtração, multiplicação, 
#             divisão, divisão inteira, resto e exponenciação. 


# - - - - > Exemplos de Operadores Aritméticos:
print("\nExemplos de Operadores Aritméticos:")

x: int = 100
y: int = 5

# - - - > Operador aritmético de adição (+):
resultado = x + y

print("\nOperador aritmético de adição (+):")
print(f"Resultado [x + y]: {resultado}")  


# - - - > Operador aritmético de subtração (-):
resultado = x - y

print("\nOperador aritmético de subtração (-):")
print(f"Resultado [x - y]: {resultado}")  


# - - -> Operador aritmético de multiplicação (*):
resultado = x * y

print("\nOperador aritmético de multiplicação (*):")
print(f"Resultado [x * y]: {resultado}")  


# - - - > Operador aritmético de divisão (/):
resultado = x / y

print("\nOperador aritmético de divisão (/):")
print(f"Resultado [x / y]: {resultado}")  


# - - - > Operador aritmético de divisão inteira (//):
resultado = x // y

print("\nOperador aritmético de divisão inteira (//):")
print(f"Resultado [x // y]: {resultado}")  


# - - - > Operador aritmético de resto ou módulo (%):
resultado = x % y

print("\nOperador aritmético de resto ou módulo (%):")
print(f"Resultado [x % y]: {resultado}")  


# - - - > Operador aritmético de exponenciação (**):
resultado = x ** y

print("\nOperador aritmético de exponenciação (**):")
print(f"Resultado [x ** y]: {resultado}")  
