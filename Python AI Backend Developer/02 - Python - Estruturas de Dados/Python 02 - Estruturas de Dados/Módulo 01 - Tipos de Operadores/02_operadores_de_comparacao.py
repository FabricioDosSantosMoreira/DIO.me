# - - - - -> Operadores de Comparação:

    # Operadores de comparação são usados para comparar valores em Python. 
    # Eles retornam True se a comparação for verdadeira e False se for falsa.


a = 5
b = 5
c = 7

# - - - -> Operador de Comparação de Igualdade (==):
print("\nOperador de igualdade (==):")
print(a == b)  
print(a == c)  
print()

# - - - -> Operador de Comparação de Desigualdade (!=):
print("\nOperador de desigualdade (!=):")
print(a != b)  
print(a != c)  
print()

# - - - -> Operador de Comparação Maior Que (>):
print("\nOperador maior que (>):")
print(c > a)  
print(a > c)  
print()

# - - - -> Operador de Comparação Menor Que (<):
print("\nOperador menor que (<):")
print(a < c)  
print(c < a)  
print()

# - - - -> Operador de Comparação Maior ou Igual Que (>=):
print("\nOperador maior ou igual que (>=):")
print(c >= a) 
print(a >= c)  
print()

# - - - -> Operador de Comparação Menor ou Igual Que (<=):
print("\nOperador menor ou igual que (<=):")
print(a <= c)  
print(c <= a) 
print()