# - - - - -> Tipos de Dados:

# Tipos de dados são categorias que especificam quais valores podem ser armazenados e manipulados. 
# Esses tipos de dados são fundamentais para manipular informações e realizar operações. 
# Cada tipo de dado possui características específicas e métodos associados 
# para manipulação e processamento.


# - - -> Tipo de dado: Inteiro (integer)
valor_int: int = 10
print("Inteiro (integer):", valor_int)
print(type(valor_int))

# - - -> Tipo de dado: Ponto Flutuante (float)
valor_float = 3.14
print("\nPonto Flutuante (float):", valor_float)
print(type(valor_float))

# - - -> Tipo de dado: Cadeia de Caracteres (string)
valor_str: str = "Olá, mundo!"
print("\nCadeia de Caracteres (string):", str)
print(type(valor_str))

# - - -> Tipo de dado: Booleano (bool)
valor_booleano = True
print("\nBooleano (bool):", valor_booleano)
print(type(valor_booleano))

# - - -> Tipo de dado: Lista (list)
valor_lista = [1, 2, 3, 4, 5]
print("\nLista (list):", valor_lista)
print(type(valor_lista))

# - - -> Tipo de dado: Tupla (tuple)
valor_tupla = (1, 2, 3, 4, 5)
print("\nTupla (tuple):", valor_tupla)
print(type(valor_tupla))

# - - -> Tipo de dado: Dicionário (dictionary)
valor_dicionario = {'a': 1, 'b': 2, 'c': 3}
print("\nDicionário (dictionary):", valor_dicionario)
print(type(valor_dicionario))

# - - -> Tipo de dado: Conjunto (set)
valor_conjunto = {1, 2, 3, 4, 5}
print("\nConjunto (set):", valor_conjunto)
print(type(valor_conjunto))

# - - -> Tipo de dado: nulo (None)
valor_nulo = None
print("\nnulo (None):", valor_nulo)
print(type(valor_nulo))

# - - -> Tipo de dado: Bytes (bytes)
valor_bytes: bytes = b'Hello'
print("\nBytes (bytes):", valor_bytes)
print(type(valor_bytes))

# - - -> Tipo de dado: Bytearray (bytearray)
valor_bytearray: bytearray = bytearray(b'Hello')
print("\nBytearray (bytearray):", valor_bytearray)
print(type(valor_bytearray))