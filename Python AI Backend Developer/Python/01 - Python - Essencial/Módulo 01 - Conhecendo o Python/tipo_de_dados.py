# Tipo de dados: Inteiro (integer)
int = 10
print("Inteiro (integer):", int)

# Tipo de dados: Ponto flutuante (float)
float = 3.14
print("Ponto flutuante (float):", float)

# Tipo de dados: Cadeia de caracteres (string)
str = "Olá, mundo!"
print("Cadeia de caracteres (string):", str)

# Tipo de dados: Booleano (bool)
booleano = True
print("Booleano (bool):", booleano)

# Tipo de dados: Lista (list)
lista = [1, 2, 3, 4, 5]
print("Lista (list):", lista)

# Tipo de dados: Tupla (tuple)
tupla = (1, 2, 3, 4, 5)
print("Tupla (tuple):", tupla)

# Tipo de dados: Dicionário (dictionary)
dicionario = {'a': 1, 'b': 2, 'c': 3}
print("Dicionário (dictionary):", dicionario)

# Tipo de dados: Conjunto (set)
conjunto = {1, 2, 3, 4, 5}
print("Conjunto (set):", conjunto)

# Tipo de dados: nulo (None)
nulo = None
print("nulo (None):", nulo)

# Tipo de dados: Bytes (bytes)
bytes_data = b'Hello'
print("Bytes (bytes):", bytes_data)

# Tipo de dados: Bytearray (bytearray)
bytearray_data = bytearray(b'Hello')
print("Bytearray (bytearray):", bytearray_data)