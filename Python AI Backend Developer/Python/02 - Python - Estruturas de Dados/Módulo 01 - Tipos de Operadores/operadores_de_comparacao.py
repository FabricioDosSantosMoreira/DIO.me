# Operadores de comparação são usados para comparar valores em Python. 
# Eles retornam True se a comparação for verdadeira e False se for falsa.


a = 5
b = 5
c = 7

# Operador de igualdade (==)
print("Operador de igualdade (==):")
print(a == b)  # Saída: True
print(a == c)  # Saída: False
print()

# Operador de desigualdade (!=)
print("Operador de desigualdade (!=):")
print(a != b)  # Saída: False
print(a != c)  # Saída: True
print()

# Operador de maior que (>)
print("Operador de maior que (>):")
print(c > a)  # Saída: True
print(a > c)  # Saída: False
print()

# Operador de menor que (<)
print("Operador de menor que (<):")
print(a < c)  # Saída: True
print(c < a)  # Saída: False
print()

# Operador de maior ou igual que (>=)
print("Operador de maior ou igual que (>=):")
print(c >= a)  # Saída: True
print(a >= c)  # Saída: False
print()

# Operador de menor ou igual que (<=)
print("Operador de menor ou igual que (<=):")
print(a <= c)  # Saída: True
print(c <= a)  # Saída: False
print()