# Operadores aritméticos são símbolos utilizados para realizar operações matemáticas básicas em variáveis ou valores. 
# Eles incluem adição, subtração, multiplicação, divisão, divisão inteira, resto e exponenciação. 


x = 10
y = 5

# Operador de adição (+)
resultado = x + y
print("Adição:", resultado)  # Saída: 15

# Operador de subtração (-)
resultado = x - y
print("Subtração:", resultado)  # Saída: 5

# Operador de multiplicação (*)
resultado = x * y
print("Multiplicação:", resultado)  # Saída: 50

# Operador de divisão (/)
resultado = x / y
print("Divisão:", resultado)  # Saída: 2.0

# Operador de divisão inteira (//)
resultado = x // y
print("Divisão Inteira:", resultado)  # Saída: 2

# Operador de resto ou módulo (%)
resultado = x % y
print("Resto:", resultado)  # Saída: 0

# Operador de exponenciação (**)
resultado = x ** y
print("Exponenciação:", resultado)  # Saída: 100000

# Prioridade de operadores
resultado = 2 + 3 * 4
print("Prioridade de operadores:", resultado)  # Saída: 14