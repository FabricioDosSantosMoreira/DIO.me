# - - - - -> Como usar o Black:

    #   Black é uma ferramenta de formatação de código Python que aplica um estilo
    # consistente ao seu código automaticamente. Ele formata o código de acordo com
    # um conjunto de regras predefinidas, garantindo que o código seja legível e
    # consistente em toda a base de código.


# - - - - -> Instalação do Black:
    #   Para instalar o Black, você pode usar o pip, o gerenciador de pacotes do Python.
    # Basta executar o seguinte comando no terminal:

    # pip install black


# - - - - -> Uso do Black:
    #   Após a instalação, você pode usar o Black para formatar seus arquivos Python.
    # Basta navegar até o diretório contendo o arquivo que deseja formatar e executar:

    # black <nome_do_arquivo.py>

    # Isso irá reformatar o arquivo Python de acordo com as regras do Black.


# - - - - -> Opções do Black:
    #   O Black oferece várias opções para personalizar o processo de formatação.
    # Alguns exemplos incluem:

    # - Modo de verificação: Use a opção "--check" para verificar se o código está
    #   formatado corretamente sem fazer alterações.
    # - Limite de largura de linha: Use a opção "--line-length" para definir o limite
    #   de largura de linha desejado.
    # - Exclusão de pastas/arquivos: Use a opção "--exclude" para especificar pastas
    #   ou arquivos a serem excluídos da formatação.


# - - - - -> Exemplo de Uso do Black:
    #   Aqui está um exemplo de como usar o Black para formatar um arquivo Python:

    # black script.py

    # Isso irá formatar o arquivo "script.py" de acordo com as regras do Black.

