# - - - - -> flake8:

# - - - - -> flake8 é uma ferramenta que analisa o código Python em busca de 
#   possíveis erros, violações de estilo e outros problemas. Ele segue as 
#   diretrizes definidas no PEP 8, o guia de estilo oficial do Python.

# - - - - -> Exemplo de Utilização do flake8:

# - - - -> Instalação:
# pip install flake8

# - - - -> Execução:
# flake8 meu_codigo.py

# - - - -> Exemplo de Saída:
# meu_codigo.py:5:1: E302 expected 2 blank lines, found 1

# - - - -> Correção:
# Adicionar uma linha em branco após a linha 4 em meu_codigo.py

# - - - -> Benefícios:
# O uso do flake8 ajuda a manter o código Python consistente e legível, 
# facilitando a colaboração entre desenvolvedores e reduzindo possíveis erros.

