# - - - - -> Operadores Aritméticos:

    # Operadores aritméticos são símbolos utilizados para realizar operações matemáticas
    # básicas em variáveis ou valores. Eles incluem: adição, subtração, multiplicação, 
    # divisão, divisão inteira, resto e exponenciação. 


x = 10
y = 5

# - - - -> Operador Aritméticos de Adição (+):
resultado = x + y
print("\nAdição:", resultado)  

# - - - -> Operador Aritméticos de Subtração (-):
resultado = x - y
print("\nSubtração:", resultado)

# - - - -> Operador Aritméticos de Multiplicação (*):
resultado = x * y
print("\nMultiplicação:", resultado)  

# - - - -> Operador Aritméticos de Divisão (/):
resultado = x / y
print("\nDivisão:", resultado)

# - - - -> Operador Aritméticos de Divisão Inteira (//):
resultado = x // y
print("\nDivisão Inteira:", resultado)  

# - - - -> Operador Aritméticos de Resto ou Módulo (%):
resultado = x % y
print("\nResto:", resultado)  

# - - - -> Operador Aritméticos de Exponenciação (**):
resultado = x ** y
print("\nExponenciação:", resultado)  
