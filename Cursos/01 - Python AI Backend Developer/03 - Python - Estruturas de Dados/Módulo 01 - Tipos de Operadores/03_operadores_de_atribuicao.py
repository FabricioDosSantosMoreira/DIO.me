# - - - - -> Operadores de Atribuição:

    # Operadores de atribuição são utilizados para atribuir valores a variáveis.
    # Eles permitem associar um valor a uma variável de forma simples e direta. 


# - - - -> Operador de Atribuição Simples (=):
x = 10 
print("Valor de x após atribuição simples:", x)


# - - - -> Operadores de Atribuição Combinada. (+=, -=, *=, /=, %=, **/, //=):
# OBS: Estes operadores combinam atribuição com uma operação aritmética

# - - -> Operador de Atribuição e Adição (+=):
x = 10 
x += 5  # Equivalente a x = x + 5
print("Valor de x após atribuição e adição:", x) 

# - - -> Operador de Atribuição e Subtração (-=):
x = 10 
x -= 3  # Equivalente a x = x - 3
print("Valor de x após atribuição e subtração:", x)

# - - -> Operador de Atribuição e Multiplicação (*=):
x = 10 
x *= 2  # Equivalente a x = x * 2
print("Valor de x após atribuição e multiplicação:", x)

# - - -> Operador de Atribuição e Divisão (/=):
x = 10 
x /= 4  # Equivalente a x = x / 4
print("Valor de x após atribuição e divisão:", x)

# - - -> Operador de Atribuição e Resto da Divisão (%):
x = 10 
x %= 2  # Equivalente a x = x % 2
print("Valor de x após atribuição e resto da divisão:", x) 

# - - -> Operador de Atribuição e Potenciação (**):
x = 10 
x **= 3  # Equivalente a x = x ** 3
print("Valor de x após atribuição e potenciação:", x)

# - - -> Operador de Atribuição e Divisão Inteira (//):
x = 10 
x //= 2  # Equivalente a x = x // 2
print("Valor de x após atribuição e divisão inteira:", x)

# - - -> Operador de Atribuição de Concatenação:
texto = "Olá"
texto += " mundo!"  # Equivalente a texto = texto + " mundo!"
print("Valor de texto após atribuição e concatenação:", texto) 
