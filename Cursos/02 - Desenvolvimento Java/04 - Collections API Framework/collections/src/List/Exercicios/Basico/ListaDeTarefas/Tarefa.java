package List.Exercicios.Basico.ListaDeTarefas;

public class Tarefa {

    // Atributo
    private String descricao;
    
    // Construtor
    public Tarefa(String descricao) {
        this.descricao = descricao;
    }

    // Getter e Setter
    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }    
}
